public class City {
	
	private String serialId;
	private String cityId;
	private String cityName;
	private String cityUpId;
	private String cityUpIdNum;
	private String cityPath;
	private String cityType;
	private String cityTypeNum;
	private String shortName;
	private String spell;
	private String areaId;
	private String postCode;
	
	public String getSerialId() {
		return serialId;
	}
	public void setSerialId(String serialId) {
		this.serialId = serialId;
	}
	public String getCityId() {
		return cityId;
	}
	public void setCityId(String cityId) {
		this.cityId = cityId;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getCityUpId() {
		return cityUpId;
	}
	public void setCityUpId(String cityUpId) {
		this.cityUpId = cityUpId;
	}
	public String getCityUpIdNum() {
		return cityUpIdNum;
	}
	public void setCityUpIdNum(String cityUpIdNum) {
		this.cityUpIdNum = cityUpIdNum;
	}
	public String getCityPath() {
		return cityPath;
	}
	public void setCityPath(String cityPath) {
		this.cityPath = cityPath;
	}
	public String getCityType() {
		return cityType;
	}
	public void setCityType(String cityType) {
		this.cityType = cityType;
	}
	public String getCityTypeNum() {
		return cityTypeNum;
	}
	public void setCityTypeNum(String cityTypeNum) {
		this.cityTypeNum = cityTypeNum;
	}
	public String getShortName() {
		return shortName;
	}
	public void setShortName(String shortName) {
		this.shortName = shortName;
	}
	public String getSpell() {
		return spell;
	}
	public void setSpell(String spell) {
		this.spell = spell;
	}
	public String getAreaId() {
		return areaId;
	}
	public void setAreaId(String areaId) {
		this.areaId = areaId;
	}
	public String getPostCode() {
		return postCode;
	}
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	
}
