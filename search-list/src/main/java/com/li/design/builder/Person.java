package com.li.design.builder;

/**
 * 人【产品类】
 * @author lichuan
 *
 */
public class Person {
	
	//头
	private String head;	
	//身体	
	private String body;
	//脚
	private String foot;
	
	public String getHead() {
		return head;
	}
	public void setHead(String head) {
		this.head = head;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public String getFoot() {
		return foot;
	}
	public void setFoot(String foot) {
		this.foot = foot;
	}
	
	
	

}
